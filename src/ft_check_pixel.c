#include "fdf.h"

int	ft_check_pixel(t_2d_point *point_2d)
{
	if (point_2d->x >= 0 && point_2d->x <= 3000
		&& point_2d->y >= 0 && point_2d->y <= 1500)
		return (0);
	else
		return (1);
}
